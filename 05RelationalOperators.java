public class RelationalOperators {
    public static void main(String[] args) {
        // = assignment operator
        // == comparitor
        // != not equal
        // <=,>=

        int x=5;
        int y=7;

        boolean result=x<=y;
        boolean result1=x!=y;
        boolean result3=x==y;
        
        System.out.println(result3);
        System.out.println(result1);
        System.out.println(result);
    }
}
